//==============================================================================
// setup sprite & load graphics
//
//==============================================================================
void charspriteplace(aSprite *sprite) {
//------------------------------------------------------------------------------
	
	
	// sprite PALETTE LOAD
	dmaCopy(sprite->palette,spritepal(sprite->paletteno),32);
	// setup playersprite
	sprite->memloc = oamAllocateGfx(&oamMain, sprite->size2, SpriteColorFormat_16Color);
    
	//LOAD SPRITE
	//THIS MISSING DMA COPY as NOT WORKING
        int p = sprite->frame*(sprite->size);
        for(unsigned int i=0;i<(sprite->size/4);i++) {
   		 sprite->memloc[i] =  sprite->discloc[p]|(sprite->discloc[(p+1)]<<4)|(sprite->discloc[(p+2)]<<8)|(sprite->discloc[(p+3)]<<12);
        	 p+=4;
        }
        updatesprite(sprite);

}
