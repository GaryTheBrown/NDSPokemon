#include "pokemon.h"
#define spritepal(p) (SPRITE_PALETTE + (p) * 16)

//==============================================================================
// setup sprite system 
// 
//==============================================================================
void initspritesystem(){
//------------------------------------------------------------------------------

//setup sprite system
	oamInit(&oamMain, SpriteMapping_1D_256, false);
	
	// setup and start player sprite
	clearcharsprite();
	initplayersprite();
	// setup and start char sprites (16)
	initcharsprite();

	//sprite priority
	//player first
	updateplayerspritepriority();
	//then chars
	for (unsigned int i = 0; i < maptouse->Spritedatasize; i++){
		updatecharspritepriority(i);
	}
}
//==============================================================================
// update priority of the player sprite 
// 
//==============================================================================
void updateplayerspritepriority(){
//------------------------------------------------------------------------------
	if (maptouse->Spritedatasize > 0){
		for (unsigned int i = 0; i < maptouse->Spritedatasize; i++){
			if (((playerspritedata.screen_y > charspritedata[i].screen_y)&&(playerspritedata.id > charspritedata[i].id))||((playerspritedata.screen_y < charspritedata[i].screen_y)&&(playerspritedata.id < charspritedata[i].id))){
				unsigned char swap;
				swap = playerspritedata.id;
				playerspritedata.id = charspritedata[i].id;
				charspritedata[i].id = swap;
				updatesprite(&playerspritedata);
				updatesprite(&charspritedata[i]);
			}
		}
	}
}

//==============================================================================
// update priority of the char sprites 
// 
//==============================================================================
void updatecharspritepriority(unsigned char n){
//------------------------------------------------------------------------------
	if (maptouse->Spritedatasize > 0){
		if (((playerspritedata.screen_y > charspritedata[n].screen_y)&&(playerspritedata.id > charspritedata[n].id))||((playerspritedata.screen_y < charspritedata[n].screen_y)&&(playerspritedata.id < charspritedata[n].id))){
				unsigned char swap;
				swap = playerspritedata.id;
				playerspritedata.id = charspritedata[n].id;
				charspritedata[n].id = swap;
				updatesprite(&playerspritedata);
				updatesprite(&charspritedata[n]);
			}
			
		for (unsigned int i = 0; i < maptouse->Spritedatasize; i++){
			if (n != i){
				if (((charspritedata[n].screen_y < charspritedata[i].screen_y)&&(charspritedata[n].id < charspritedata[i].id))||((charspritedata[n].screen_y > charspritedata[i].screen_y)&&(charspritedata[n].id > charspritedata[i].id))){
					unsigned char swap;
					swap = charspritedata[n].id;
					charspritedata[n].id = charspritedata[i].id;
					charspritedata[i].id = swap;
					updatesprite(&charspritedata[n]);
					updatesprite(&charspritedata[i]);
				}
			}
		}
	}
}

//==============================================================================
// updates the sprite priority 
// 
//==============================================================================
void updatespritepriority(aSprite *sprite,unsigned char priority){
//------------------------------------------------------------------------------
	
	sprite->priority = priority;
	
	oamSet(&oamMain, sprite->id, sprite->screen_x, sprite->screen_y, sprite->priority, sprite->paletteno, sprite->size2, SpriteColorFormat_16Color, sprite->memloc, 0,false, sprite->hidden, false, false, false);
}

//==============================================================================
// updates the sprites 
// 
//==============================================================================
void updatesprite(aSprite *sprite){
//------------------------------------------------------------------------------
	oamSet(&oamMain, sprite->id, sprite->screen_x, sprite->screen_y, sprite->priority, sprite->paletteno, sprite->size2, SpriteColorFormat_16Color, sprite->memloc, 0,false, sprite->hidden, false, false, false);
}

//==============================================================================
// Clears the NPC's sprites 
// 
//==============================================================================
void clearcharsprite(){	
//------------------------------------------------------------------------------
	for (unsigned int i = 0; i < 32; i++){
			oamClear (&oamMain, charspritedata[i].id,0);
			charspritedata[i].id = i+1;
			charspritedata[i].screen_y = 0;
			 
		
	}
}

//==============================================================================
// Checks if NPC's are out of screen + 1 
// (+1 helps keep sprites from flashing up)
//==============================================================================
void checkcharvisible(){
//------------------------------------------------------------------------------
	for (unsigned int i = 0; i < maptouse->Spritedatasize; i++){
			if((maptouse->Spritedata[i].map_x < map_player_x - 9)||(maptouse->Spritedata[i].map_x > map_player_x + 9)||(maptouse->Spritedata[i].map_y < map_player_y - 7)||(maptouse->Spritedata[i].map_y > map_player_y + 7)){
				charspritedata[i].hidden = true;
			}else{
				charspritedata[i].hidden = not(maptouse->Spritedata[i].visible);
			}
	}
}
//==============================================================================
// setup sprite chars NPC's
//
//==============================================================================
void initcharsprite(){
//------------------------------------------------------------------------------
	for (unsigned int i = 0; i < 32; i++){
		charspritedata[i].hidden = true;
	}
	
	
	for (unsigned int i = 0; i < maptouse->Spritedatasize; i++){
		if((maptouse->Spritedata[i].map_x < map_player_x - 9)||(maptouse->Spritedata[i].map_x > map_player_x + 9)||(maptouse->Spritedata[i].map_y < map_player_y - 7)||(maptouse->Spritedata[i].map_y > map_player_y + 7)){
			charspritedata[i].hidden = true;
		}else{
			charspritedata[i].hidden = not(maptouse->Spritedata[i].visible);
		}
		charspritedata[i].id = (i+1);
		charspritedata[i].size = 16*32;
		charspritedata[i].size2 = SpriteSize_16x32;
		charspritedata[i].paletteno = 1;
		charspritedata[i].priority = 2;
		charspritedata[i].palette = (unsigned short*)maptouse->Spritedata[i].spritepal;
		charspritedata[i].frame = ((maptouse->Spritedata[i].direction-1)*3);
		charspritedata[i].discloc = (unsigned char*)maptouse->Spritedata[i].spritetiles;
		//work out location
		charspritedata[i].screen_x = (maptouse->Spritedata[i].map_x - map_player_x) * 16 + 120;
		charspritedata[i].screen_y = (maptouse->Spritedata[i].map_y - map_player_y) * 16 + 72;
		charspriteplace(&charspritedata[i]);
	
	}
		
}
